    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        
        String drug = txtDrug.getText();
        String alcohol = txtAlcohol.getText();
        String opiod = txtOpiod.getText();
        String toxins = txtToxins.getText();
        String rbc = txtRBC.getText();
        String wbc = txtWBC.getText();
        try{
            
            int d = Integer.parseInt(drug);
            int a = Integer.parseInt(alcohol);
            int o = Integer.parseInt(opiod);
            int t = Integer.parseInt(toxins);
            int r = Integer.parseInt(rbc);
            int w = Integer.parseInt(wbc);
            
            request.setTestResult(jComboBox1.getSelectedItem()+"");
            request.setStatus("Completed");
            request.setAlcohol(alcohol);
            request.setDrug(drug);
            request.setOpiod(opiod);
            request.setToxins(toxins);
            request.setRbc(rbc);
            request.setWbc(wbc);
            
            JOptionPane.showMessageDialog(null, "Test Result Updated Successfully!");
            
            //next
            userProcessContainer.remove(this);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            Component[] comps = this.userProcessContainer.getComponents();
            for(Component comp: comps){
            if(comp instanceof LabAssistantWorkAreaJPanel){
                LabAssistantWorkAreaJPanel area = (LabAssistantWorkAreaJPanel)comp;
                area.populateTable();
            }
        }
        layout.previous(userProcessContainer);
            
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Please Enter a valid number!");
            return;
        }
        
    }//GEN-LAST:event_submitJButtonActionPerformed

